x = 10
y = 5

# Aritmetic Operator
# add
print('x + y =',x+y)

# sun
print('x - y =',x-y)

# mul
print('x * y =',x*y)

# div
print('x / y =',x/y)

# floor div
print('x // y =',x//y)

# Exponentiation
print('x ** y =',x**y)

# Comparision operator

print('x > y is',x>y)

print('x != y is',x!=y)

print('x <= y is',x<=y)

# Logical operator

x = True
y = False

print('x and y is',x and y)

print('x or y is',x or y)

print('not x is',not x)

# Identity operator

x = 5
y = 5

print(x is not y)

print(x is y)

# Membership operators

x = 'Hello world!'

print('H' in x)

print('hello' not in x)
